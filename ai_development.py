# -*- coding: utf-8 -*-
"""AI發展歷史介紹程式

本程式會列出人工智慧相關的重大發展事件，
並依照時間排序顯示。
"""

from textwrap import dedent

AI_TIMELINE = [
    (1950, "艾倫·圖靈提出圖靈測試，為AI研究奠定理論基礎"),
    (1956, "達特茅斯會議標誌著人工智慧作為研究領域的誕生"),
    (1966, "ELIZA聊天機器人問世，展示了自然語言處理的可能性"),
    (1987, "專家系統開始在產業界廣泛應用"),
    (1997, "IBM深藍擊敗西洋棋世界冠軍卡斯帕羅夫"),
    (2011, "IBM華生在益智節目《危險邊緣》戰勝人類冠軍"),
    (2012, "深度學習在ImageNet競賽上大幅提升圖像辨識準確率"),
    (2016, "AlphaGo擊敗圍棋世界冠軍李世乭"),
    (2023, "生成式AI(如ChatGPT)引發全球關注")
]

def show_history():
    print(dedent("""
    =====================
      人工智慧發展大事記
    =====================
    """))
    for year, event in AI_TIMELINE:
        print(f"{year} - {event}")

    print(dedent("""

    AI技術仍持續快速演進，
    透過不斷的研究與應用，
    人工智慧正逐漸融入我們的生活。
    """))

if __name__ == "__main__":
    show_history()
